"foo" // -> primitive
 3.1415 // -> primitive
[ 'a', 'b', 'c' ] // -> No, an array is a container object
 false // -> primitive
foo // -> No, foo is an identifier
function bar() { return "bar"; } // -> No, functions are objects
undefined // -> primitive
